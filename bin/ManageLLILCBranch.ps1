function Global:MgSetBuildType([string] $buildtype)
{
    $buildtype = MgExpandBuildType $buildtype
    $mgll.buildtype = $buildtype
    $mgll.llvmbld = MgFullBuildDir($mgll.llvmbldbase)
    $env:LLVMBUILD = $mgll.llvmbld
    $mgll.buildcmakeoptions = ""
    if ($buildtype -eq "RelWithDebInfo") {
        $mgll.buildcmakeoptions = "-DLLVM_ENABLE_ASSERTIONS=ON"
    }
    if ($mgll.uni) {
        $mgll.llilcjit = Join-Path $mgll.llvmbld "bin/$($buildtype)/llilcjit.dll"
    }
    else {
        $mgll.llilcbld = MgFullBuildDir($mgll.llilcbldbase)
        $mgll.llilcjit = Join-Path $mgll.llilcbld "bin/$($buildtype)/llilcjit.dll"
        if ($mgll.clangp) {
            $mgll.clangbld = MgFullBuildDir($mgll.clangbldbase)
        }
    }
    # Make build directories.
    MgMkBldDir($buildtype)
}

