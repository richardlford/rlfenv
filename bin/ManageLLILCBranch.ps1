# Initialize a LLILC workspace
#
# \param $wk           Path to the workspace
# \param $buildtype    Debug, Release, or RelWithDebInfo
# \param $user         User's GitHub handle.
# \param $branch       Branch on which modifications are to be made.
# \param $uni          True if in unified LLVM mode (i.e. LLILC and CLANG in LLVM repo)
# \param $clangp       True if include clang.
# \param $devversion   12 or 14.
# 
function Global:MgInit([string]$wk, [string]$buildtype, [string]$user, [string]$branch, 
                       [bool]$uni, [bool]$clangp, [string]$devVersion)
{
    $buildtype = MgExpandBuildType $buildtype
    $Global:mgll = @{}
    if ($user -eq "") {
        throw "You must specify your github user handle";
    }
    $mgll.user = $user

    if ($wk -eq "") {
        throw "You must use specify the location of the workspace";
    }
    $mgll.work = $wk

    if ($buildtype -eq "") {
        throw "You must use specify the build type";
    }
    $mgll.buildtype = $buildtype


    if ($branch -eq "") {
        throw "You must use specify the branch";
    }
    $mgll.branch = $branch
    $mgll.uni = $uni
    $mgll.clangp = $clangp

    if (($devVersion -ne "120") -and ($devVersion -ne "140")) {
        throw "Unsupported dev version: $devVersion"
    }
    $mgll.DevVersion = $devVersion

    $mgll.clrsrc = Join-Path $wk "coreclr"
    $env:CORECLRSOURCE = $mgll.clrsrc
    $mgll.clrtests = Join-Path $mgll.clrsrc "tests"
    $mgll.clrbld = Join-Path $mgll.clrsrc "bin/Product/Windows_NT.x64.Debug"

    $mgll.roslynsrc = Join-Path $wk "roslyn"
    $mgll.roslynrun = Join-Path $wk "rosrun"
    $mgll.rosportable = Join-Path $mgll.roslynsrc "src/Compilers/CSharp/Portable"
    $mgll.rosrsp = Join-Path $wk  "rsps/Microsoft.CodeAnalysis.CSharp.DLl.rsp"
    $mgll.roscsc = Join-Path $mgll.roslynrun "csc.exe"
    $mgll.roscorerun = Join-Path $mgll.roslynrun "corerun.exe"
  
    $mgll.windbg = "c:/dbg64/windbg.exe"

    $mgll.llvmsrc = Join-Path $wk "llvm"
    $env:LLVMSOURCE = $mgll.llvmsrc
    $mgll.llvmtoolssrc = Join-Path $mgll.llvmsrc "tools"
    $mgll.llvmbldbase = Join-Path $wk "llvm-build"

    if ($mgll.uni) {
        # Unified paths
        $mgll.llilcsrc = Join-Path $mgll.llvmsrc "tools/llilc"
        $env:LLILCSOURCE = $mgll.llilcsrc
        if ($clangp) {
            $mgll.clangsrc = Join-Path $mgll.llvmsrc "tools/clang"
        }
    }
    else {
        # Separate path
        $mgll.llilcsrc = Join-Path $wk "llilc"
        $mgll.llilcbldbase = Join-Path $wk "llilc-build"
        if ($clangp) {
            $mgll.clangsrc = Join-Path $wk "clang";
            $mgll.clangbldbase = Join-Path $wk "clang-build"
        }
    }
    if ($clangp) {
        $mgll.extrasrc = Join-Path $mgll.clangsrc "tools/extra"
    }

    $mgll.runtestpy = Join-Path $mgll.llilcsrc "test/llilc_runtest.py"
    $mgll.test = Join-Path $wk test
    $mgll.testdir = Join-Path $wk "target"

    $baseBranch = @{}
    $baseBranch.llvm = "MS"
    $baseBranch.llilc = "master"
    if ($clangp) {
        $baseBranch.clang = "MS"
        $baseBranch.extra = "MS"
    }
    $mgll.baseBranchMap = $baseBranch

    $src = @{}
    $src.llvm = $mgll.llvmsrc
    $src.llilc = $mgll.llilcsrc
    if ($clangp) {
        $src.clang = $mgll.clangsrc
        $src.extra = $mgll.extrasrc
    }
    $mgll.src = $src

    MgSetBuildType $buildtype
    Write-Output("mgll is ")
    $mgll | Out-Default
    MgMkWk
    Push-Location $mgll.work
}


