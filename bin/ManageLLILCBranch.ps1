#### Cmake functions

# Unified Cmake
function Global:MgCmLLVM
{
  Push-Location $mgll.llvmbld
  if ($mgll.uni) {
      cmake -G "Visual Studio 12 2013 Win64" $mgll.llvmsrc "-DWITH_CORECLR=$($mgll.clrbld)" -DLLVM_ENABLE_DOXYGEN=ON -DLLVM_TARGETS_TO_BUILD:STRING=X86 "-DCMAKE_BUILD_TYPE=$($mgll.buildtype)" $mgll.buildcmakeoptions $args
  }
  else {
      cmake -G "Visual Studio 12 2013 Win64" $mgll.llvmsrc -DLLVM_ENABLE_DOXYGEN=ON -DLLVM_TARGETS_TO_BUILD:STRING=X86 "-DCMAKE_BUILD_TYPE=$($mgll.buildtype)" $mgll.buildcmakeoptions $args
  }
  Pop-Location
}

function Global:MgCmLLILC
{
  if ($mgll.uni) {
      throw "MgCmLLILC: Not configured for separate cmake"
  }
  Push-Location $mgll.llilcbld
  cmake -G "Visual Studio 12 2013 Win64" $mgll.llilcsrc "-DWITH_CORECLR=$($mgll.clrbld)" "-DWITH_LLVM=$($mgll.llvmbld)" -DLLVM_TARGETS_TO_BUILD:STRING=X86 -DLLVM_ENABLE_DOXYGEN=ON "-DCMAKE_BUILD_TYPE=$($mgll.buildtype)" $mgll.buildcmakeoptions
  Pop-Location
}

function Global:MgCmClang
{
  if ($mgll.uni) {
      throw "MgCmClang: Not configured for separate cmake"
  }
  Push-Location $mgll.clangbld
  # Put llvm-config on path so clang can find it.
  $savePath = $env:path
  $llvmConfigPath = Join-Path $mgll.llvmbld "Debug\bin"
  $env:path =  $llvmConfigPath + ";" + $env:path
  cmake -G "Visual Studio 12 2013 Win64" $mgll.clangsrc "-DCMAKE_BUILD_TYPE=$($mgll.buildtype)" $mgll.buildcmakeoptions $args
  $env:path = $savePath
  Pop-Location
}

