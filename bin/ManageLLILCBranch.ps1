function Global:MgCopyRoslyn
{
    $roslynWild = Join-Path $mgll.roslynsrc "Binaries\Debug\core-clr\*"
    Copy-Item -Force -Recurse $roslynWild -Destination $mgll.roslynrun
    $coreclrExeWild = Join-Path $mgll.clrbld "*.exe"
    Copy-Item -Force $coreclrExeWild -Destination $mgll.roslynrun
    $coreclrDllWild = Join-Path $mgll.clrbld "*.dll"
    Copy-Item -Force -Recurse $coreclrDllWild -Destination $mgll.roslynrun
    # Copy csc.exe to csc.dll and CoreConsole.exe to csc.exe. 
    # CoreConsole.exe (named csc.exe) masquerades as csc.exe by starting
    # up the CoreClr and then loading and executing csc.dll (really the original csc.exe).
    $cscexepath = Join-Path $mgll.roslynrun "csc.exe"
    $cscdllpath = Join-Path $mgll.roslynrun "csc.dll"
    $coreconsolepath = Join-Path $mgll.roslynrun "CoreConsole.exe"
    Copy-Item -Force $cscexepath $cscdllpath
    Copy-Item -Force $coreconsolepath $cscexepath
}

